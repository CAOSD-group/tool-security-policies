def load_feature_dict(csv_file="../resources/mapping_csv/kubernetes_mapping_properties_features.csv"):
    """
    Devuelve un diccionario:
        midle -> row completa del CSV
    donde 'Midle' es la columna con nombres tipo:
        Pod_spec_containers_securityContext_runAsUser
    """
    feature_dict = {}
    with open(csv_file, newline="", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        for row in reader:
            midle = row["Midle"].strip()
            feature_dict[midle] = row
    return feature_dict


def load_kinds_prefix_mapping(file_path: str) -> dict:
    """
    Create a dict {Kind: Prefix} from the CSV generated by mappingUvlCsvK8sOld.py
    Ex:
        {"Pod": "io_k8s_api_core_v1_Pod",
         "Deployment": "io_k8s_api_apps_v1_Deployment", ...}
    """
    mapping = {}
    with open(file_path, newline="", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        for row in reader:
            kind = row["Kind"].strip()
            prefix = row["Prefix"].strip()
            mapping[kind] = prefix
    return mapping


def normalize_kind(kind: str, kind_map: dict) -> str:
    """
    Normaliza el nombre del kind usando el mapa de prefijos.
    Si no se encuentra, capitaliza por defecto.
    """
    k_lower = kind.lower()
    for k in kind_map.keys():
        if k.lower() == k_lower:
            return k
    return kind.capitalize()


